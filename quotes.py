def get_quote_of_the_day(dt=None):
    """根据日期返回当天的名言"""
    if dt is None:
        from datetime import date
        dt = date.today()

    day_of_year = dt.timetuple().tm_yday
    index = (dt.year * 12 + day_of_year) % len(ALL_QUOTES)
    return ALL_QUOTES[index]


def get_daily_tips(dt=None):
    """返回每日四条行动建议（饮食/睡眠/心情/运动各一条）"""
    if dt is None:
        from datetime import date
        dt = date.today()

    day_of_year = dt.timetuple().tm_yday
    return {
        category: tips[(dt.year * 7 + day_of_year + i) % len(tips)]
        for i, (category, tips) in enumerate(ALL_TIPS.items())
    }
